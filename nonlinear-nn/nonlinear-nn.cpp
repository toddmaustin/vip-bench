VIP_ENCDOUBLE
ReLU(VIP_ENCDOUBLE x)
{
  VIP_ENCDOUBLE ret; 
#ifdef VIP_DO_MODE 
  ret = VIP_CMOV((x > 0), (x), (VIP_ENCDOUBLE)0);
#else
  ret = (x > 0) ? x : 0;
#endif
  return ret;
}

VIP_ENCDOUBLE
LeakyReLU(VIP_ENCDOUBLE x)
{
  VIP_ENCDOUBLE ret; 
#ifdef VIP_DO_MODE 
  ret = (VIP_CMOV((x > 0), x, A*x));
#else
  ret = (x > 0) ? x : A*x;
#endif
  return ret;
}




